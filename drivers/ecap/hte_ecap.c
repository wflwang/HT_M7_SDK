void ECAP_setEmulationMode(ECAP_Type *base, ECAP_EmulationMode_t mode)
{
    assert(ECAP_isBaseValid(base));

    /* Write to FREE/SOFT bit */
    base->ECCTL1 = (base->ECCTL1 & (~ECAP_ECCTL1_FREE_SOFT_Msk)) | ((uint16_t)mode << ECAP_ECCTL1_FREE_SOFT_Pos);
}
